package com.home.demos.PrepInsta_Training;

public class FloatingPoint {

    public static void main(String[] args) {

        int intValue = 2345;
        float floatValue = 10f / 3;
        double doubleValue = 1000d / 7;

        /**  After putting f & d we wil get the precise value of the division
         *   Also double is more standard used as global datatype as it
         *   gives more precise value  **/

        System.out.println("intValue: " +intValue);
        System.out.println("floatValue: " +floatValue);
        System.out.println("doubleValue: " +doubleValue);

    }
}
